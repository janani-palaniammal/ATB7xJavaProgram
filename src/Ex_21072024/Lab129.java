package Ex_21072024;

public class Lab129 {
    public static void main(String[] args) {
        int i=10;
        System.out.println("-----while loop-----");
        while (i<10) {
            System.out.println(i);
            i++;
        }

        System.out.println("\n------do while loop-------");
        do {
            System.out.println(i);
            i++;
        } while(i<10);
    }
}
