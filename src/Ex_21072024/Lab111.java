package Ex_21072024;

public class Lab111 {
    public static void main(String[] args) {
        //Printing numbers in reverse order
        for (int i=10; i>=0; i--) { //decrement
            System.out.println("Numbers: "+i);
        }

        for (char c='o'; c>= 'a'; c--) {
            System.out.println("Alphabets: "+c);
        }
    }
}
