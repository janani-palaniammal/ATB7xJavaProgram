package Ex_13072024;

public class Lab027 {
    public static void main(String[] args) {
        // Escape Chars
        char c = '\n'; // New line / Next Line
        char c1 = '\t'; // Add Tab
        char c2 = '\b'; // Backlash, delete 1 char
        char c3 = '\r'; // Backlash, delete 1 word
        System.out.println("Heart" + c + "Beat");
        System.out.println("Heart" + c1 + "Beat");
        System.out.println("Heart" + c2 + "Beat");
        System.out.println("Heart" + c3 + "Beat");
    }
}
